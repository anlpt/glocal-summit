-- ---------- Tables ----------
create table if not exists groups (
  id          bigint generated by default as identity primary key,
  name        text not null,
  color       text not null default '#cc2027',
  sort_order  int  not null default 0
);

create table if not exists labs (
  id          bigint generated by default as identity primary key,
  group_id    bigint not null references groups(id) on delete cascade,
  name        text not null,
  sort_order  int  not null default 0
);

create table if not exists participants (
  id            bigint generated by default as identity primary key,
  stt           text,
  title         text,
  name          text not null,
  country       text,
  org           text,
  org_logo_url  text,
  role          text,
  email         text not null unique,
  coordinator   text,
  group_id      bigint references groups(id) on delete set null,
  self_registered boolean not null default false
);

create table if not exists selections (
  id              bigint generated by default as identity primary key,
  participant_id  bigint not null references participants(id) on delete cascade,
  lab_id          bigint not null references labs(id) on delete cascade,
  created_at      timestamptz not null default now(),
  unique (participant_id, lab_id)
);

create table if not exists responses (
  id              bigint generated by default as identity primary key,
  participant_id  bigint not null unique references participants(id) on delete cascade,
  answer          text not null,
  updated_at      timestamptz not null default now()
);

create table if not exists settings (
  key    text primary key,
  value  text
);

